/// Generates demo book Korean content.
String generateDemoContent() {
  final buf = StringBuffer();
  for (int c = 0; c < demoChapterTitles.length; c++) {
    buf.writeln('제${c + 1}장: ${demoChapterTitles[c]}');
    buf.writeln();
    for (int p = 0; p < 30; p++) {
      buf.writeln(
        '${_demoSentences[(c + p) % _demoSentences.length]} '
        '${_demoMidSentences[(c + p) % _demoMidSentences.length]} '
        '${_demoDetails[(p * 2 + c) % _demoDetails.length]} '
        '${_demoEndings[(c + p) % _demoEndings.length]}',
      );
      buf.writeln();
    }
    buf.writeln();
  }
  return buf.toString();
}
